// LOGIC AND CONTROL FLOW



//If -Else Conditions

const age = 18;
if (age > 18) {
    console.log("You may Enter !");
} else if (age == 18) {
    console.log("You just turned 18 , Welcome!");
} else {
    console.log("You are not allowed !");
}



//Loops
let i = 0;
while (i < 10) {
    console.log("i = " + i);
    i++;
}

for (let i = 0; i < 10; i++) {
    console.log("for For loop ,i is " + i);
}
