// BY USING ELSE-IF CONDITIONAL STATEMENT

let mode="light";           //default value
let color;

if(mode==="light"){
    color="white";          //if mode=light ,then put value "white" in color container
    }
else if(mode==="dark"){
    color="black";          //if mode=dark ,then put value "black" in color container
    }
else{
    color="blue";           //if mode is other ,then put value "blue" in color container
}
console.log(color);         //TO print actual value of color container